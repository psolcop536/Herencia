
package formas;

class Punto {
    /** @autor Pablo Soler
     * @ since 2024
     * @version 1.0
     * @param x
     * @param y
     * @see https://github.com/PabloSoler
     */
    private double x, y;

    public Punto(double x, double y) {
        /** Constructor de la clase Punto
         */
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}


